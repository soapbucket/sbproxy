// Package rule implements rule-based request matching using conditions, expressions, and pattern matching.
package rule

// Helper functions for equality checking

func stringSliceEqual(a, b []string) bool {
	if len(a) != len(b) {
		return false
	}
	for i := range a {
		if a[i] != b[i] {
			return false
		}
	}
	return true
}
