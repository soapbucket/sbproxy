// --- Ed25519 path ---
//
// We reproduce the canonical signature base in this test rather than
// reaching into `sbproxy-middleware` so the test exercises a truly
// independent signer. The format mirrors RFC 9421 §2 byte-for-byte:
// each covered component on its own line, then the
// `"@signature-params"` line.

use base64::Engine;
use ed25519_dalek::{Signer, SigningKey};
use rand::rngs::OsRng;
use rand::RngCore;

/// Build the canonical signature base for `GET /` with the supplied
/// inner-list and parameter section. Mirrors
/// `sbproxy_middleware::signatures::build_signature_base` for the
/// `@method` + `@target-uri` subset that this test covers.
fn build_base_for_get_root(inner_list: &str, params: &str) -> String {
    let mut out = String::new();
    out.push_str("\"@method\": GET\n");
    out.push_str("\"@target-uri\": /\n");
    out.push_str("\"@signature-params\": (");
    out.push_str(inner_list);
    out.push(')');
    if !params.is_empty() {
        out.push(';');
        out.push_str(params);
    }
    out
}

/// Build a bot_auth config whose single agent verifies Ed25519 with
/// the supplied verifying-key hex string.
fn ed25519_config(upstream_url: &str, verifying_key_hex: &str) -> String {
    format!(
        r#"
proxy:
  http_bind_port: 0
origins:
  "blog.localhost":
    action:
      type: proxy
      url: "{upstream_url}"
    authentication:
      type: bot_auth
      clock_skew_seconds: 9999999999
      agents:
        - name: ed25519-bot
          key_id: ed-bot-1
          algorithm: ed25519
          public_key: "{verifying_key_hex}"
          required_components:
            - "@method"
            - "@target-uri"
"#
    )
}

#[test]
fn valid_ed25519_signature_returns_200() {
    // Generate a fresh keypair so the test does not depend on any
    // hard-coded key material. The verifier is configured to trust
    // exactly this public key.
    let mut secret_bytes = [0u8; 32];
    OsRng.fill_bytes(&mut secret_bytes);
    let signing_key = SigningKey::from_bytes(&secret_bytes);
    let verifying_key_hex = hex::encode(signing_key.verifying_key().to_bytes());

    let upstream = MockUpstream::start(json!({"ok": true})).expect("upstream");
    let harness =
        ProxyHarness::start_with_yaml(&ed25519_config(&upstream.base_url(), &verifying_key_hex))
            .expect("start");

    let inner_list = r#""@method" "@target-uri""#;
    let params = r#"created=1700000000;keyid="ed-bot-1";alg="ed25519""#;
    let base = build_base_for_get_root(inner_list, params);

    let signature = signing_key.sign(base.as_bytes());
    let sig_b64 = base64::engine::general_purpose::STANDARD.encode(signature.to_bytes());

    let signature_input = format!("sig1=({});{}", inner_list, params);
    let signature_header = format!("sig1=:{}:", sig_b64);

    let resp = harness
        .get_with_headers(
            "/",
            "blog.localhost",
            &[
                ("signature-input", signature_input.as_str()),
                ("signature", signature_header.as_str()),
            ],
        )
        .expect("send");
    assert_eq!(
        resp.status,
        200,
        "valid Ed25519 signature should authorize, got {}: {:?}",
        resp.status,
        resp.text().unwrap_or_default()
    );
    assert!(!upstream.captured().is_empty());
}

#[test]
fn tampered_ed25519_signature_returns_401() {
    let mut secret_bytes = [0u8; 32];
    OsRng.fill_bytes(&mut secret_bytes);
    let signing_key = SigningKey::from_bytes(&secret_bytes);
    let verifying_key_hex = hex::encode(signing_key.verifying_key().to_bytes());

    let upstream = MockUpstream::start(json!({"ok": true})).expect("upstream");
    let harness =
        ProxyHarness::start_with_yaml(&ed25519_config(&upstream.base_url(), &verifying_key_hex))
            .expect("start");

    let inner_list = r#""@method" "@target-uri""#;
    let params = r#"created=1700000000;keyid="ed-bot-1";alg="ed25519""#;
    let base = build_base_for_get_root(inner_list, params);
    let signature = signing_key.sign(base.as_bytes());

    // Flip the first byte to produce a syntactically valid Ed25519
    // signature that will fail the verify check. The verifier should
    // reject with "cryptographic verification failed".
    let mut sig_bytes = signature.to_bytes();
    sig_bytes[0] ^= 0xff;
    let sig_b64 = base64::engine::general_purpose::STANDARD.encode(sig_bytes);

    let signature_input = format!("sig1=({});{}", inner_list, params);
    let signature_header = format!("sig1=:{}:", sig_b64);

    let resp = harness
        .get_with_headers(
            "/",
            "blog.localhost",
            &[
                ("signature-input", signature_input.as_str()),
                ("signature", signature_header.as_str()),
            ],
        )
        .expect("send");
    assert_eq!(
        resp.status, 401,
        "tampered Ed25519 signature must be rejected"
    );
    assert!(upstream.captured().is_empty());
}

// --- Non-root path coverage (W4-C / F1.6 regression) ---
//
// Earlier waves only verified the happy path on `/`. The
// reconstruction in `check_auth` previously hardcoded `@target-uri`
// to `/`, so a signature bound to `/api/foo?x=1` was either rejected
// when valid or accepted when bound to a different path. These two
// tests exercise the live request pipeline with a non-root path so
// the round-trip through Pingora's `req_header().uri.path() +
// .uri.query()` is covered end-to-end.

/// HMAC-SHA256 sign the canonical base for `GET <target_uri>` with
/// the supplied raw `Signature-Input` line. Mirrors RFC 9421 §2 byte
/// for byte for the `@method` + `@target-uri` subset; identical to
/// the helper in the `sbproxy-core` unit tests.
fn sign_for_target(secret_hex: &str, target_uri: &str, sig_input: &str) -> String {
    use hmac::{KeyInit, Mac};
    type HmacSha256 = hmac::Hmac<sha2::Sha256>;

    let mut base = String::new();
    base.push_str("\"@method\": GET\n");
    base.push_str("\"@target-uri\": ");
    base.push_str(target_uri);
    base.push('\n');
    base.push_str("\"@signature-params\": ");
    // Strip the leading `sig1=` label so what we hash is the inner
    // list + params, matching the verifier's canonical base.
    let inner = sig_input
        .strip_prefix("sig1=")
        .expect("test signature-input must start with sig1=");
    base.push_str(inner);

    let key_bytes = hex::decode(secret_hex).expect("hex secret");
    let mut mac = <HmacSha256 as KeyInit>::new_from_slice(&key_bytes).expect("mac");
    mac.update(base.as_bytes());
    let sig = mac.finalize().into_bytes();
    base64::engine::general_purpose::STANDARD.encode(sig)
}

#[test]
fn bot_auth_accepts_signature_bound_to_real_request_path() {
    // Sign against `/api/foo?x=1` and send the live request to the
    // same path-and-query. The proxy must reconstruct
    // `@target-uri = /api/foo?x=1` so the verifier reproduces the
    // signer's base.
    let upstream = MockUpstream::start(json!({"ok": true})).expect("upstream");
    let harness = ProxyHarness::start_with_yaml(&hmac_config(&upstream.base_url())).expect("start");

    let secret_hex = "736861726564626f74736563726574";
    let sig_input =
        r#"sig1=("@method" "@target-uri");created=1700000000;keyid="bot-key-1";alg="hmac-sha256""#;
    let sig_b64 = sign_for_target(secret_hex, "/api/foo?x=1", sig_input);
    let sig_header = format!("sig1=:{}:", sig_b64);

    let resp = harness
        .get_with_headers(
            "/api/foo?x=1",
            "blog.localhost",
            &[("signature-input", sig_input), ("signature", &sig_header)],
        )
        .expect("send");
    assert_eq!(
        resp.status,
        200,
        "signature bound to /api/foo?x=1 must verify against the live request, got {}: {:?}",
        resp.status,
        resp.text().unwrap_or_default()
    );
    assert!(!upstream.captured().is_empty());
}

