//! Web Bot Auth (RFC 9421 HTTP Message Signatures).
//!
//! The provider verifies a per-request signature against a directory
//! of known agents. Today the directory supports `ed25519` and
//! `hmac_sha256` algorithms. The HMAC-SHA256 path uses values
//! pre-computed offline (documented below) so the suite has a
//! deterministic baseline; the Ed25519 path generates a fresh keypair
//! at runtime via `ed25519-dalek` and signs the canonical base in
//! the test itself, which is the strongest possible coverage shape.
//!
//! The verifier reconstructs the canonical "signature base" from the
//! live request per RFC 9421 §2 and runs the configured algorithm
//! over `(base, raw_signature)`. Our pre-computed signature was
//! generated by:
//!
//! ```python
//! import hmac, hashlib, base64
//! base = (
//!     '"@method": GET\n'
//!     '"@target-uri": /\n'
//!     '"@signature-params": ("@method" "@target-uri");'
//!     'created=1700000000;keyid="bot-key-1";alg="hmac-sha256"'
//! )
//! key = bytes.fromhex("736861726564626f74736563726574")  # "sharedbotsecret"
//! sig = hmac.new(key, base.encode(), hashlib.sha256).digest()
//! print(base64.b64encode(sig).decode())
//! # => 2iFjyCUlIZVN/xib7q/lEQEIsrLGbwc5K/6PZu2F6Ys=
//! ```
//!
//! `created` is in the past, which the verifier accepts (only future
//! `created` values fail freshness). No `expires` is set. The HMAC
//! key is supplied as a hex string so `decode_secret` reliably ends
//! up with the same 15 bytes both at signing time (Python) and at
//! verification time (the proxy).
//!
//! We sign for the root path `/` because that is the most stable
//! `@target-uri` value the proxy reconstructs. The e2e harness sends
//! every request to a small static-action origin so the request line
//! is `GET / HTTP/1.1`.

use sbproxy_e2e::{MockUpstream, ProxyHarness};
use serde_json::json;

const SIGNATURE_INPUT: &str =
    r#"sig1=("@method" "@target-uri");created=1700000000;keyid="bot-key-1";alg="hmac-sha256""#;
/// HMAC-SHA256 of the canonical signature base computed in the
/// module-level docstring. base64-standard encoded.
const VALID_SIG_B64: &str = "2iFjyCUlIZVN/xib7q/lEQEIsrLGbwc5K/6PZu2F6Ys=";

fn hmac_config(upstream_url: &str) -> String {
    format!(
        r#"
proxy:
  http_bind_port: 0
origins:
  "blog.localhost":
    action:
      type: proxy
      url: "{upstream_url}"
    authentication:
      type: bot_auth
      clock_skew_seconds: 9999999999
      agents:
        - name: test-bot
          key_id: bot-key-1
          algorithm: hmac_sha256
          public_key: "736861726564626f74736563726574"
          required_components:
            - "@method"
            - "@target-uri"
"#
    )
}

