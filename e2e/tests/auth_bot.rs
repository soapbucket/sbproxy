// ===========================================================================
// Q1.4: dynamic Web Bot Auth directory cache
// ===========================================================================
//
// Per `docs/adr-bot-auth-directory.md` (A1.3), the directory is a JWKS
// fetched from a `https://` URL with a self-signature, refreshed on
// the configured TTL with stale-while-fail and negative-caching
// semantics. Implementation lands on `wave1/G1.6-G1.7-metrics-botauth`.
//
// Until G1.7 merges, the static-directory tests above continue to
// cover the verification path; the dynamic-directory tests below are
// `#[ignore]`d with a `TODO(wave1-G1.7)` reason. We keep them in this
// file (rather than spinning up a second test crate) so that all
// bot-auth coverage lives in one navigable surface.
//
// Each test boots a tiny `axum` mock directory on `127.0.0.1:0` with
// per-test knobs (cache headers, signature validity, JWKS rotation,
// etc.) so the failure modes from the ADR are individually exercisable.

mod directory_q1_4 {
    // `super::*` is reserved for tests landing on G1.7; the inner
    // module is currently self-contained.
    #[allow(unused_imports)]
    use super::*;
    use axum::{
        extract::State,
        http::{HeaderMap, StatusCode},
        response::IntoResponse,
        routing::get,
        Json, Router,
    };
    use serde_json::{json, Value};
    use std::net::SocketAddr;
    use std::sync::atomic::{AtomicUsize, Ordering};
    use std::sync::Arc;
    use tokio::net::TcpListener;
    use tokio::sync::Mutex;

    /// Knobs the tests dial on a per-instance basis. Some knobs are
    /// reserved for tests landing on G1.7 (Cache-Control validation,
    /// self-signature flip, custom Content-Type) and intentionally
    /// unused today.
    #[derive(Debug, Default, Clone)]
    #[allow(dead_code)]
    pub struct DirKnobs {
        /// Body bytes to serve. When `None`, fall back to a synthesized
        /// minimal JWKS with one Ed25519 key.
        pub body: Option<Vec<u8>>,
        /// `Content-Type` header on the directory response.
        pub content_type: Option<String>,
        /// `Cache-Control` header on the directory response.
        pub cache_control: Option<String>,
        /// Status code to serve. Default 200.
        pub status: Option<u16>,
        /// When `Some(n)`, serve `n` failures before any success.
        pub fail_first_n: usize,
        /// When `true`, the body is signed with a key NOT in the body
        /// (self-signature invalid).
        pub self_signature_invalid: bool,
    }

    #[derive(Debug, Default)]
    pub struct DirState {
        pub knobs: DirKnobs,
        pub fetches: AtomicUsize,
        pub last_headers: Mutex<HeaderMap>,
    }

    pub async fn spawn_mock_directory(knobs: DirKnobs) -> (SocketAddr, Arc<DirState>) {
        let state = Arc::new(DirState {
            knobs,
            ..Default::default()
        });
        let app = Router::new()
            .route(
                "/.well-known/http-message-signatures-directory",
                get(handler),
            )
            .with_state(state.clone());
        let listener = TcpListener::bind("127.0.0.1:0").await.expect("bind");
        let addr = listener.local_addr().expect("local_addr");
        tokio::spawn(async move {
            let _ = axum::serve(listener, app.into_make_service()).await;
        });
        (addr, state)
    }

    async fn handler(State(state): State<Arc<DirState>>, headers: HeaderMap) -> impl IntoResponse {
        *state.last_headers.lock().await = headers;
        let n = state.fetches.fetch_add(1, Ordering::SeqCst);

        if n < state.knobs.fail_first_n {
            return (
                StatusCode::INTERNAL_SERVER_ERROR,
                Json(json!({"error": "fail_first_n knob"})),
            );
        }
        let status =
            StatusCode::from_u16(state.knobs.status.unwrap_or(200)).unwrap_or(StatusCode::OK);
        let body: Value = state
            .knobs
            .body
            .as_ref()
            .and_then(|b| serde_json::from_slice(b).ok())
            .unwrap_or_else(|| json!({"keys": []}));
        (status, Json(body))
    }

    /// Tokio runtime helper. Each test owns its own current-thread
    /// runtime so the mock directory and the proxy harness live in
    /// independent process-local schedulers.
    pub fn rt() -> tokio::runtime::Runtime {
        tokio::runtime::Builder::new_current_thread()
            .enable_all()
            .build()
            .expect("runtime")
    }

    /// Origin config that wires bot_auth's directory at `directory.url`.
    /// G1.7 will lock the exact YAML keys; the names below mirror the
    /// ADR text so a maintainer dropping `#[ignore]` can also rename
    /// in one place.
    pub fn bot_auth_directory_config(directory_url: &str) -> String {
        format!(
            r#"
proxy:
  http_bind_port: 0
origins:
  "blog.localhost":
    action:
      type: static
      status_code: 200
      content_type: text/plain
      body: "ok"
    authentication:
      type: bot_auth
      directory:
        url: "{directory_url}"
        refresh_interval: 24h
        negative_cache_ttl: 5m
        stale_grace: 24h
        require_self_signature: true
        signature_agents:
          allow:
            - "{directory_url}"
"#
        )
    }
}

