//! Pluggable SSE usage parsers for AI providers.
//!
//! Different providers report token usage with different SSE shapes
//! (OpenAI puts a terminal `usage` chunk on the wire, Anthropic
//! splits it across `message_start` / `message_delta`, Vertex emits
//! `usageMetadata`, Bedrock wraps each Anthropic chunk in a
//! base64-encoded `bytes` envelope, Cohere uses an `event-type`
//! taxonomy, Ollama emits line-delimited JSON). The streaming relay
//! in `sbproxy-core` does not want to know about any of those
//! shapes; it just needs a parser to feed bytes into and ask for
//! totals at stream close.
//!
//! The [`SseUsageParser`] trait is the contract. One implementation
//! per provider lives under `usage_parser::<provider>`; the
//! [`select_parser`] entry point returns a boxed parser based on the
//! configured `usage_parser` field plus runtime hints (upstream URL,
//! response Content-Type, optional `X-Provider` header).
//!
//! All implementations buffer at most a single SSE line so streams of
//! arbitrary length never accumulate the full response body in
//! memory. Parse failures and missing usage fields are silently
//! ignored: streams routinely interleave keep-alive comments and
//! events without any usage payload, and a malformed upstream chunk
//! must never abort the relay.

