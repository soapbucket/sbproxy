//! RFC 9421 HTTP Message Signatures: sign-on-egress.
//!
//! Companion to [`crate::signatures`], which implements the inbound
//! verification half. This module produces the `Content-Digest`,
//! `Signature-Input`, and `Signature` headers an outbound request
//! needs so a downstream verifier (the existing
//! [`MessageSignatureVerifier`](crate::signatures::MessageSignatureVerifier),
//! or any Web Bot Auth compatible origin) can confirm the request was
//! signed by the holder of the configured workspace key.
//!
//! [Web Bot Auth]: https://datatracker.ietf.org/doc/draft-meunier-web-bot-auth-architecture/
//!
//! # Scope
//!
//! - Algorithms: `ed25519` (primary, the Web Bot Auth profile) and
//!   `hmac-sha256` (secondary).
//! - Default covered components: `@method`, `@target-uri`, `@authority`,
//!   and `content-digest` when the request carries a body.
//! - Optional `Content-Digest` emission via
//!   [`compute_content_digest`](crate::digest::compute_content_digest)
//!   (RFC 9530 `sha-256` by default). If the request has a body and
//!   `content-digest` is in the covered set, the signer adds the header
//!   itself; this keeps the signed-payload binding intact even if the
//!   caller forgot to compute the digest.
//! - The `created` parameter is set to the current wall-clock time and
//!   the `keyid` + `alg` parameters reflect the
//!   [`SignerConfig`](crate::signatures_egress::SignerConfig).
//!
//! # What this module is not
//!
//! - Workspace key management. The signer takes the secret bytes
//!   verbatim; how the operator stores it (Vault, env, KMS) is a
//!   separate concern.
//! - Pingora outbound-hook wiring. This module is a pure function: in
//!   takes a `http::Request<bytes::Bytes>`, out comes the same request
//!   with three signature headers attached.
//!
//! # Example
//!
//! ```
//! use bytes::Bytes;
//! use sbproxy_middleware::signatures_egress::{MessageSigner, SignerConfig};
//! use sbproxy_middleware::signatures::SignatureAlgorithm;
//!
//! // Random Ed25519 key generated by the operator; in a real
//! // deployment this comes from workspace key material.
//! let secret = [0u8; 32];
//! let cfg = SignerConfig {
//!     key_id: "workspace-egress-1".to_string(),
//!     algorithm: SignatureAlgorithm::Ed25519,
//!     secret: secret.to_vec(),
//!     covered_components: Vec::new(), // empty = use defaults
//! };
//! let signer = MessageSigner::new(cfg).unwrap();
//!
//! let req = http::Request::builder()
//!     .method("POST")
//!     .uri("https://api.example.com/v1/items")
//!     .body(Bytes::from_static(b"{}"))
//!     .unwrap();
//! let signed = signer.sign_request(req).unwrap();
//! assert!(signed.headers().contains_key("signature"));
//! assert!(signed.headers().contains_key("signature-input"));
//! assert!(signed.headers().contains_key("content-digest"));
//! ```

