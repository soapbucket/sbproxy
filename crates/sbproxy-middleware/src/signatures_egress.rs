    // --- Body change after signing breaks verification ---

    #[test]
    fn tampering_with_body_after_signing_breaks_verification() {
        let (signer, verifier) = signer_and_verifier_ed25519();
        let signed = signer.sign_request(sample_post_request()).expect("sign");

        // Replace the body on the wire but keep all three signature
        // headers. Because content-digest is part of the covered set
        // and binds the body, the verifier must reject this even
        // though Signature-Input/Signature are still present.
        let (parts, _body) = signed.into_parts();
        let tampered = Request::from_parts(parts, Bytes::from_static(b"{\"name\":\"sabotage\"}"));

        // The tampered request still carries the original
        // content-digest header (which now mismatches the new body),
        // so the digest verifier would catch it. But the signature
        // covers the original content-digest header too: changing the
        // body would force the caller to recompute content-digest,
        // which would invalidate the signature. We exercise that path
        // here by recomputing the digest header to match the new body
        // and confirming the signature still fails.
        let new_digest = compute_content_digest(Algorithm::Sha256, tampered.body().as_ref());
        let mut headers = tampered.headers().clone();
        headers.insert(
            "content-digest",
            HeaderValue::from_str(&new_digest).unwrap(),
        );
        let (mut parts, body) = tampered.into_parts();
        parts.headers = headers;
        let rebuilt = Request::from_parts(parts, body);

        match verifier.verify_request(&rebuilt) {
            VerifyVerdict::Ok { .. } => {
                panic!("verifier accepted a request with a tampered body")
            }
            VerifyVerdict::Failed { reason } => {
                assert!(
                    reason.contains("cryptographic"),
                    "expected cryptographic failure, got: {reason}"
                );
            }
        }
    }

    // --- created timestamp freshness ---

    #[test]
    fn created_parameter_is_recent() {
        let (signer, _verifier) = signer_and_verifier_ed25519();
        let before = SystemTime::now()
            .duration_since(UNIX_EPOCH)
            .unwrap()
            .as_secs() as i64;
        let signed = signer.sign_request(sample_post_request()).expect("sign");
        let after = SystemTime::now()
            .duration_since(UNIX_EPOCH)
            .unwrap()
            .as_secs() as i64;

        let header = signed
            .headers()
            .get("signature-input")
            .expect("signature-input present")
            .to_str()
            .unwrap();
        let parsed = parse_signature_input(header).expect("parse");
        let entry = &parsed[0].1;
        let created = entry.params.created.expect("created present");
        // Within +/- 10s of "now". The bounds are inclusive because the
        // signer reads its own `now` between `before` and `after`.
        assert!(
            created >= before - 10,
            "created {created} < before {before}"
        );
        assert!(created <= after + 10, "created {created} > after {after}");
    }

    // --- Bodyless requests ---

    #[test]
    fn bodyless_request_signs_without_content_digest() {
        // A GET with no body should sign cleanly: the signer must drop
        // content-digest from the covered set rather than fail.
        let (signer, verifier) = signer_and_verifier_ed25519();
        let signed = signer.sign_request(sample_get_request()).expect("sign");

        assert!(
            !signed.headers().contains_key("content-digest"),
            "bodyless request should not carry content-digest"
        );

        let sig_input = signed
            .headers()
            .get("signature-input")
            .unwrap()
            .to_str()
            .unwrap();
        assert!(
            !sig_input.contains("\"content-digest\""),
            "covered set should not list content-digest for bodyless requests, got: {sig_input}"
        );

        match verifier.verify_request(&signed) {
            VerifyVerdict::Ok { .. } => {}
            VerifyVerdict::Failed { reason } => panic!("expected ok, got: {reason}"),
        }
    }

    // --- Pre-computed content-digest is preserved ---

    #[test]
    fn signer_preserves_caller_supplied_content_digest() {
        let (signer, _verifier) = signer_and_verifier_ed25519();
        // A caller (perhaps a streaming pipeline that used DigestSink
        // chunk by chunk) computed content-digest itself. The signer
        // must not overwrite it.
        let pre_existing = compute_content_digest(Algorithm::Sha256, b"{\"name\":\"widget\"}");
        let req = Request::builder()
            .method("POST")
            .uri("https://api.example.com/v1/items")
            .header("host", "api.example.com")
            .header("content-digest", &pre_existing)
            .body(Bytes::from_static(b"{\"name\":\"widget\"}"))
            .unwrap();
        let signed = signer.sign_request(req).expect("sign");
        assert_eq!(
            signed
                .headers()
                .get("content-digest")
                .unwrap()
                .to_str()
                .unwrap(),
            pre_existing
        );
    }

    // --- Custom covered components ---

    #[test]
    fn custom_covered_components_are_honoured() {
        let (_, _) = signer_and_verifier_hmac();
        let secret_hex = "00112233445566778899aabbccddeeff";
        let secret = hex::decode(secret_hex).unwrap();
        let signer = MessageSigner::new(SignerConfig {
            key_id: "workspace-1".to_string(),
            algorithm: SignatureAlgorithm::HmacSha256,
            secret,
            covered_components: vec!["@method".to_string(), "@path".to_string()],
        })
        .unwrap();
        let signed = signer.sign_request(sample_post_request()).expect("sign");
        let sig_input = signed
            .headers()
            .get("signature-input")
            .unwrap()
            .to_str()
            .unwrap();
        assert!(sig_input.contains("\"@method\""));
        assert!(sig_input.contains("\"@path\""));
        assert!(!sig_input.contains("\"content-digest\""));
        // The verifier still accepts it because none of these
        // components were tampered.
        let verifier = MessageSignatureVerifier::new(MessageSignatureConfig {
            algorithm: SignatureAlgorithm::HmacSha256,
            key_id: "workspace-1".to_string(),
            key: secret_hex.to_string(),
            required_components: Vec::new(),
            clock_skew_seconds: 30,
        })
        .unwrap();
        match verifier.verify_request(&signed) {
            VerifyVerdict::Ok { .. } => {}
            VerifyVerdict::Failed { reason } => panic!("expected ok, got: {reason}"),
        }
    }

